package ru.kyrianov_aleksey.electrical_circuit_by_kirchhoff;

public class Coord {
    private int x;
    private int y;

    public Coord(int y, int x) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
